def clone_repo():
    """Clone the dashboard repo."""
    token = os.environ["GITHUB_TOKEN"]
    repo_url = f"https://x-access-token:{token}@github.com/mwebster0111/life-optimiser-dashboard.git"

    if os.path.exists(REPO_DIR):
        subprocess.run(["rm", "-rf", REPO_DIR])

    subprocess.run(
        ["git", "clone", repo_url, REPO_DIR],
        check=True, capture_output=True
    )
    # Set git identity
    subprocess.run(["git", "config", "user.email", "actions@github.com"], cwd=REPO_DIR)
    subprocess.run(["git", "config", "user.name", "Life Optimiser Bot"], cwd=REPO_DIR)


def push_changes(message):
    """Commit and push changes to the repo."""
    subprocess.run(["git", "add", "-A"], cwd=REPO_DIR, check=True)
    result = subprocess.run(
        ["git", "diff", "--cached", "--quiet"],
        cwd=REPO_DIR, capture_output=True
    )
    if result.returncode == 0:
        print("No changes to commit.")
        return
    subprocess.run(["git", "commit", "-m", message], cwd=REPO_DIR, check=True)
    subprocess.run(["git", "push"], cwd=REPO_DIR, check=True)
    print(f"Pushed: {message}")


def build_activities_data(activities):
    """Convert Garmin activities to dashboard format."""
    result = []
    for a in activities[:10]:
        name = a.get("activityName", "Activity")
        dist = round(a.get("distance", 0) / 1000, 2)
        dur_secs = a.get("duration", 0)
        if dur_secs > 3600:
            dur = f"{int(dur_secs // 3600)}h {int((dur_secs % 3600) // 60)}m"
        else:
            dur = f"{int(dur_secs // 60)}m {int(dur_secs % 60)}s"

        avg_hr = a.get("averageHR", 0) or 0
        max_hr = a.get("maxHR", 0) or 0
        cals = a.get("calories", 0) or 0

        # Pace for running activities
        pace = ""
        if dist > 0 and "run" in name.lower():
            pace_secs = dur_secs / dist
            pace = f"{int(pace_secs // 60)}:{int(pace_secs % 60):02d}/km"

        result.append({
            "date": a.get("startTimeLocal", "")[:10],
            "name": name,
            "dist": dist,
            "duration": dur,
            "avgHR": avg_hr,
            "maxHR": max_hr,
            "pace": pace,
            "calories": cals,
        })
    return result


def build_running_data(training, activities):
    """Build running stats for dashboard."""
    ts = training.get("training_status") or {}
    rp = training.get("race_predictions")

    # Parse race predictions
    predictions = {"fiveK": "--:--", "tenK": "--:--", "half": "--:--:--", "marathon": "--:--:--"}
    if rp:
        pred_list = rp if isinstance(rp, list) else [rp]
        for pred in pred_list:
            if isinstance(pred, dict):
                for key in pred:
                    val = pred[key]
                    if isinstance(val, dict):
                        secs = val.get("predictedTimeSeconds") or val.get("time")
                        if secs:
                            h = int(secs // 3600)
                            m = int((secs % 3600) // 60)
                            s = int(secs % 60)
                            formatted = f"{h}:{m:02d}:{s:02d}" if h > 0 else f"{m}:{s:02d}"
                            dist = val.get("distance") or val.get("raceDistance", {}).get("distance", 0)
                            if dist:
                                dist_km = dist / 1000 if dist > 100 else dist
                                if abs(dist_km - 5) < 1:
                                    predictions["fiveK"] = formatted
                                elif abs(dist_km - 10) < 1:
                                    predictions["tenK"] = formatted
                                elif abs(dist_km - 21.1) < 1:
                                    predictions["half"] = formatted
                                elif abs(dist_km - 42.2) < 1:
                                    predictions["marathon"] = formatted

    # Weekly running stats
    week_km = 0
    week_runs = 0
    today = datetime.date.fromisoformat(today_str())
    week_start = today - datetime.timedelta(days=today.weekday())
    for a in activities:
        act_date = a.get("startTimeLocal", "")[:10]
        if act_date >= week_start.isoformat() and "run" in (a.get("activityName", "") or "").lower():
            week_km += (a.get("distance", 0) or 0) / 1000
            week_runs += 1

    acute = ts.get("acuteTrainingLoad", 0) or 0
    chronic = ts.get("chronicTrainingLoad", 0) or 0
    ratio = round(acute / chronic, 2) if chronic > 0 else 0

    load_status = "Optimal"
    if ratio > 1.5:
        load_status = "High Risk"
    elif ratio > 1.3:
        load_status = "Caution"
    elif ratio < 0.8:
        load_status = "Detraining"

    return {
        "prs": {"fiveK": "--:--", "tenK": "--:--"},
        "predictions": predictions,
        "vo2max": ts.get("vo2MaxValue", 0) or 0,
        "weeklyKm": round(week_km, 1),
        "weekRuns": week_runs,
        "trainingStatus": ts.get("trainingStatusPhrase", "Unknown"),
        "acuteLoad": round(acute),
        "chronicLoad": round(chronic),
        "loadRatio": ratio,
        "loadStatus": load_status,
        "balance": ts.get("trainingLoadBalancePhrase", "N/A") or "N/A",
    }


def build_schedule_data(events, date_info):
    """Convert calendar events to dashboard schedule format."""
    today = today_str()
    tomorrow = (datetime.date.fromisoformat(today) + datetime.timedelta(days=1)).isoformat()
    day_after = (datetime.date.fromisoformat(today) + datetime.timedelta(days=2)).isoformat()

    days = {}
    for event in events:
        start = event["start"]
        event_date = start[:10] if len(start) >= 10 else ""

        if event_date == today:
            label = "Today"
        elif event_date == tomorrow:
            dt = datetime.date.fromisoformat(tomorrow)
            label = dt.strftime("%A")
        elif event_date == day_after:
            dt = datetime.date.fromisoformat(day_after)
            label = dt.strftime("%A")
        else:
            continue

        if label not in days:
            days[label] = []

        time_str = ""
        if not event["all_day"]:
            time_str = start[11:16] if len(start) > 11 else ""

        # Color dot based on event type
        summary = event["summary"].lower()
        if any(w in summary for w in ["thesis", "study", "work", "everllence"]):
            dot = "var(--purple)"
        elif any(w in summary for w in ["run", "train", "gym", "exercise"]):
            dot = "var(--green)"
        elif any(w in summary for w in ["meet", "call", "sync"]):
            dot = "var(--blue)"
        else:
            dot = "var(--orange)"

        days[label].append({
            "time": time_str,
            "name": event["summary"],
            "dot": dot,
        })

    # Sort and format
    order = ["Today"]
    if tomorrow:
        dt = datetime.date.fromisoformat(tomorrow)
        order.append(dt.strftime("%A"))
    if day_after:
        dt = datetime.date.fromisoformat(day_after)
        order.append(dt.strftime("%A"))

    result = []
    for label in order:
        if label in days:
            result.append({"label": label, "events": days[label]})

    return result


