def inject_data_into_html(html, data):
    """Replace the DATA block in the dashboard HTML."""
    data_json = json.dumps(data, indent=2, ensure_ascii=False)
    new_block = f"{DATA_START_MARKER}\nconst DATA = {data_json};\n{DATA_END_MARKER}"

    # Find and replace the data block
    pattern = re.escape(DATA_START_MARKER) + r'.*?' + re.escape(DATA_END_MARKER)
    new_html = re.sub(pattern, new_block, html, flags=re.DOTALL)

    if new_html == html and DATA_START_MARKER not in html:
        raise ValueError("Could not find DATA block markers in HTML!")

    return new_html


def main():
    date_info = copenhagen_now()
    print(f"Dashboard Update starting — {date_info['full_output']}")

    # Clone repo
    print("Cloning dashboard repo...")
    clone_repo()

    # Read current HTML
    html_path = os.path.join(REPO_DIR, "index.html")
    with open(html_path, "r") as f:
        html = f.read()

    # Pull all data
    print("Connecting to Garmin...")
    garmin = get_garmin_client()
    today = today_str()
    fourteen_days_ago = (datetime.date.fromisoformat(today) - datetime.timedelta(days=14)).isoformat()

    print("Pulling Garmin data...")
    stats = get_garmin_stats(garmin, today)
    training = get_garmin_training_status(garmin)
    activities = get_garmin_activities(garmin, fourteen_days_ago, today)
    trends = get_garmin_week_data(garmin, 7)

    print("Pulling calendar...")
    try:
        events = get_gcal_events(days_ahead=3)
    except Exception as e:
        print(f"Warning: Calendar failed: {e}")
        events = []

    print("Pulling weather...")
    weather = get_weather(7)

    # Build structured data
    activities_data = build_activities_data(activities)
    running_data = build_running_data(training, activities)
    schedule = build_schedule_data(events, date_info)

    # Generate AI content
    print("Generating AI content via Claude...")
    garmin_summary = json.dumps({
        "stats": stats.get("stats", {}),
        "sleep": stats.get("sleep", {}).get("dailySleepDTO", {}),
    }, default=str)
    ai_content = generate_ai_content(garmin_summary, trends, activities, events, weather, running_data)

    # Build full DATA object
    data = build_full_data(trends, activities_data, running_data, schedule, weather, ai_content, date_info)

    # Inject into HTML
    print("Injecting data into dashboard...")
    new_html = inject_data_into_html(html, data)

    # Write and push
    with open(html_path, "w") as f:
        f.write(new_html)

    print("Pushing to GitHub Pages...")
    push_changes(f"Dashboard update — {date_info['day_of_week']} {today}")

    print("Dashboard update complete!")
    print(f"View at: https://mwebster0111.github.io/life-optimiser-dashboard/")


if __name__ == "__main__":
    main()
